"""Топологическая оптимизация балки через RL: среда с МКЭ.

ЧТО ЗДЕСЬ ПРОИСХОДИТ (для тех, кто не знаком с темой)
-----------------------------------------------------
Топологическая оптимизация — это «где в детали оставить материал, а где
выкинуть». Классический пример — кости: по закону Вольфа кость рассасывает
ненагруженную ткань и укрепляет нагруженную, получая максимум прочности
на грамм массы. Здесь агент делает то же самое с балкой.

ПОСТАНОВКА: балка 150x30x12 мм на двух опорах, 400 Н в центре.
Моделируем ПОЛОВИНУ балки (задача симметрична относительно середины
пролёта — это вдвое дешевле по вычислениям) сеткой квадратных клеток
1.25 мм. Каждая клетка либо «материал» (1), либо «пустота» (0).

ФИЗИКА — МКЭ (метод конечных элементов, англ. FEM): деталь режется на
маленькие элементы, для каждого известна связь «силы <-> перемещения»
(матрица жёсткости), из них собирается большая разреженная система
уравнений K*u = F, её решение u — перемещения всех узлов сетки.
Здесь: плоское напряжённое состояние, билинейные четырёхузловые элементы.

ЭПИЗОД RL: агент за NUM_STEPS шагов «рассасывает» слабонагруженный
материал. Действие на каждом шаге — два числа:
  * доля удаляемого материала на этом шаге,
  * «температура» — насколько жадно резать именно самые ненагруженные
    клетки (низкая температура = строго по рангу нагруженности,
    высокая = больше случайности = органичнее структура).

НАГРАДА в конце эпизода: жёсткость итоговой структуры (насколько мало
она прогибается) при заданной доле оставшегося материала (~45%).
"""

import collections

import numpy as np
import scipy.sparse as sp
import scipy.sparse.linalg as spla

# ---- Геометрия и материал (PLA) -------------------------------------------
SPAN_MM = 150.0          # пролёт балки, мм
HEIGHT_MM = 30.0         # высота балки, мм
THICK_MM = 12.0          # толщина (глубина) детали, мм — по ней считаем
                         # плоское напряжённое состояние
ELEM_MM = 1.25           # размер клетки сетки, мм
DESIGN_LOAD_N = 400.0    # полный груз в центре (~40 кг)

E0 = 3300.0              # модуль упругости PLA, МПа
NU = 0.36                # коэффициент Пуассона (поперечное сжатие при растяжении)
E_VOID = 1e-6 * E0       # «пустым» клеткам даём крошечную жёсткость вместо нуля,
                         # чтобы матрица системы не вырождалась (стандартный приём)
SIGMA_ULT_MPA = 50.0     # предел прочности PLA
DENSITY_G_MM3 = 1.24e-3  # плотность PLA, г/мм^3

NX = int(round(SPAN_MM / 2 / ELEM_MM))   # 60 клеток по X: половина пролёта
NY = int(round(HEIGHT_MM / ELEM_MM))     # 24 клетки по Y: высота

TARGET_VOL = 0.45        # цель: оставить ~45% материала
NUM_STEPS = 25           # шагов «резьбы» в эпизоде


# ---- МКЭ: матрица жёсткости элемента (гауссова квадратура 2x2) -------------
def element_stiffness():
    """Матрица жёсткости 8x8 одного квадратного элемента (для E=1).

    У элемента 4 узла, у каждого 2 степени свободы (сдвиг по x и y) —
    итого 8. Матрица связывает силы в узлах с их перемещениями.
    Интеграл по площади элемента считается гауссовой квадратурой 2x2:
    вместо честного интеграла — сумма значений в 4 специальных точках
    (стандартный приём МКЭ, точный для билинейных элементов).
    """
    D = E_MAT_UNIT()
    K = np.zeros((8, 8))
    g = 1.0 / np.sqrt(3.0)               # координаты точек Гаусса
    for xi in (-g, g):
        for eta in (-g, g):
            B = b_matrix(xi, eta)
            # B^T * D * B * (площадь на точку) * толщина
            K += B.T @ D @ B * (ELEM_MM**2 / 4.0) * THICK_MM
    return K


def E_MAT_UNIT():
    """Матрица упругости плоского напряжённого состояния для E=1.

    Связывает деформации с напряжениями (закон Гука в матричном виде).
    Реальный модуль E подставляется позже умножением — так матрицу
    жёсткости элемента можно посчитать один раз, а потом просто
    масштабировать для «материала» и «пустоты».
    """
    return 1.0 / (1 - NU**2) * np.array([
        [1.0, NU, 0.0],
        [NU, 1.0, 0.0],
        [0.0, 0.0, (1 - NU) / 2.0],
    ])


def b_matrix(xi, eta):
    """B-матрица: переводит перемещения узлов элемента в деформации.

    (xi, eta) — локальные координаты внутри элемента, от -1 до 1;
    узлы обходятся против часовой стрелки начиная с (-1,-1).
    dN — производные функций формы; множитель 2/ELEM_MM — якобиан
    (пересчёт из локальных координат в миллиметры).
    """
    dN = 0.25 * np.array([
        [-(1 - eta), (1 - eta), (1 + eta), -(1 + eta)],   # dN/dxi
        [-(1 - xi), -(1 + xi), (1 + xi), (1 - xi)],       # dN/deta
    ]) * (2.0 / ELEM_MM)
    # Раскладываем производные в стандартную структуру B (3 деформации x
    # 8 перемещений): eps_x, eps_y и сдвиговая деформация gamma_xy.
    B = np.zeros((3, 8))
    B[0, 0::2] = dN[0]
    B[1, 1::2] = dN[1]
    B[2, 0::2] = dN[1]
    B[2, 1::2] = dN[0]
    return B


# Матрица жёсткости элемента одинакова для всех клеток — считаем один раз.
KE = element_stiffness()

# ---- Сетка: узлы, степени свободы, граничные условия ------------------------
N_NODES = (NX + 1) * (NY + 1)   # узлы — углы клеток
N_DOF = 2 * N_NODES             # степени свободы: по 2 на узел (сдвиг x, y)


def nid(ix, iy):
    """Номер узла по его координатам в сетке.
    iy=0 — низ балки; ix=0 — середина пролёта (мы моделируем половину)."""
    return ix * (NY + 1) + iy


# EDOF[e] — 8 номеров степеней свободы элемента e (4 узла x 2 координаты).
# Нужно, чтобы быстро собирать большую матрицу из маленьких KE.
EDOF = np.zeros((NX * NY, 8), dtype=np.int64)
for ex in range(NX):
    for ey in range(NY):
        n = [nid(ex, ey), nid(ex + 1, ey), nid(ex + 1, ey + 1), nid(ex, ey + 1)]
        EDOF[ex * NY + ey] = [d for k in n for d in (2 * k, 2 * k + 1)]

# Заготовки индексов строк/столбцов для сборки разреженной матрицы:
# каждый элемент вносит блок 8x8 = 64 значения.
_IK = np.repeat(EDOF, 8, axis=1).ravel()
_JK = np.tile(EDOF, (1, 8)).ravel()

# Граничные условия (какие перемещения запрещены):
# * симметрия: на срезе x=0 (середина пролёта) запрещён сдвиг по x —
#   зеркальная половина «держит» точно так же;
# * опора: запрещён сдвиг по y у 3 нижних узлов правого края.
fixed = [2 * nid(0, iy) for iy in range(NY + 1)]
fixed += [2 * nid(ix, 0) + 1 for ix in range(NX - 2, NX + 1)]
FIXED = np.array(sorted(set(fixed)))
FREE = np.setdiff1d(np.arange(N_DOF), FIXED)   # все остальные степени свободы

# Нагрузка: половина груза (мы считаем полбалки) вниз, размазана по
# 4 верхним узлам у середины пролёта (точечная сила на 1 узел дала бы
# нефизичный пик напряжений).
F = np.zeros(N_DOF)
for ix in range(4):
    F[2 * nid(ix, NY) + 1] = -(DESIGN_LOAD_N / 2.0) / 4.0

# Неудаляемые зоны — агенту запрещено выедать материал там, где
# прикладывается груз и где балка стоит на опоре.
PASSIVE = np.zeros((NX, NY), dtype=bool)
PASSIVE[:4, NY - 3:] = True     # площадка под груз (верх, у середины)
PASSIVE[NX - 4:, :3] = True     # пятка над опорой (низ, у правого края)


def solve(density):
    """Главный МКЭ-расчёт: решает K*u = F для текущей раскладки материала.

    Вход:  density (NX, NY) — карта материала, значения 0 (пусто) / 1 (есть).
    Выход: (compliance, energy):
      compliance — «податливость» F·u: чем МЕНЬШЕ, тем ЖЁСТЧЕ структура
                   (это главная метрика качества);
      energy (NX, NY) — энергия деформации каждой клетки: показывает,
                   насколько клетка нагружена (по ней агент решает, что резать).
    """
    # Жёсткость каждой клетки: E_VOID для пустых, E0 для сплошных.
    e_elem = (E_VOID + density.reshape(-1, order="C") * (E0 - E_VOID))
    # Собираем глобальную разреженную матрицу: KE каждого элемента,
    # умноженная на его жёсткость, раскладывается по индексам _IK/_JK.
    # (плотности идут в порядке ex*NY+ey — том же, что EDOF)
    sk = (KE.ravel()[None, :] * e_elem[:, None]).ravel()
    K = sp.coo_matrix((sk, (_IK, _JK)), shape=(N_DOF, N_DOF)).tocsc()
    # Решаем систему только по свободным степеням свободы
    # (закреплённые = 0 по определению).
    u = np.zeros(N_DOF)
    u[FREE] = spla.spsolve(K[FREE][:, FREE], F[FREE])
    # Энергия деформации клетки = 0.5 * u^T * K * u (на её перемещениях).
    ue = u[EDOF]                                       # (nel, 8)
    energy = 0.5 * np.einsum("ni,ij,nj->n", ue, KE, ue) * e_elem
    return float(F @ u), energy.reshape(NX, NY)


def von_mises(density):
    """Напряжения фон Мизеса в центрах сплошных клеток, МПа.

    Фон Мизес — стандартный способ свернуть 3 компоненты напряжений
    (растяжение по x, по y, сдвиг) в одно число, которое сравнивают с
    пределом прочности материала. Используется для финальной проверки
    прочности найденной структуры (в topo_stl.py).

    Выход: (vm (NX, NY) — карта напряжений; center_defl — прогиб центра, мм).
    """
    # Та же сборка и решение системы, что в solve().
    e_elem = (E_VOID + density.reshape(-1) * (E0 - E_VOID))
    sk = (KE.ravel()[None, :] * e_elem[:, None]).ravel()
    K = sp.coo_matrix((sk, (_IK, _JK)), shape=(N_DOF, N_DOF)).tocsc()
    u = np.zeros(N_DOF)
    u[FREE] = spla.spsolve(K[FREE][:, FREE], F[FREE])
    # Напряжения в центре элемента: sigma = D * B * u.
    B0 = b_matrix(0.0, 0.0)
    D = E_MAT_UNIT() * E0
    s = (D @ B0 @ u[EDOF].T).T                         # (nel, 3): sx, sy, txy
    # Формула фон Мизеса для плоского напряжённого состояния.
    vm = np.sqrt(s[:, 0]**2 - s[:, 0] * s[:, 1] + s[:, 1]**2 + 3 * s[:, 2]**2)
    vm = vm.reshape(NX, NY) * density                  # в пустоте не считаем
    center_defl = -u[2 * nid(0, NY) + 1]               # прогиб верхней точки центра
    return vm, center_defl


def blur(field, radius=2, sigma=1.0):
    """Гауссово сглаживание карты нагруженности.

    Классическая проблема топологической оптимизации — «шахматная доска»:
    без сглаживания решение вырождается в клетки через одну (математически
    выгодно, физически бессмысленно и непечатаемо). Размытие
    чувствительностей — стандартное лекарство.
    """
    # Одномерное гауссово ядро...
    x = np.arange(-radius, radius + 1)
    g = np.exp(-x**2 / (2 * sigma**2))
    g /= g.sum()
    # ...применяем по обеим осям (двумерная свёртка = две одномерных).
    # pad с mode="edge" продлевает края, чтобы не темнели границы.
    pad = np.pad(field, radius, mode="edge")
    out = np.zeros_like(field)
    for i, wi in enumerate(g):
        for j, wj in enumerate(g):
            out += wi * wj * pad[i:i + NX, j:j + NY]
    return out


def prune_hairs(d):
    """Убирает «волоски» — клетки, у которых меньше 2 соседей.

    Такие отростки не несут нагрузку (сила не может через них пройти
    транзитом), а при печати превращаются в мусорные ниточки.
    Повторяем до 3 раз, потому что удаление волоска может оголить
    следующий.
    """
    for _ in range(3):
        # Считаем число заполненных соседей каждой клетки (4-соседство).
        nb = np.zeros_like(d)
        nb[1:, :] += d[:-1, :]
        nb[:-1, :] += d[1:, :]
        nb[:, 1:] += d[:, :-1]
        nb[:, :-1] += d[:, 1:]
        hair = (d > 0.5) & (nb < 2) & ~PASSIVE
        if not hair.any():
            break
        d = d * ~hair
    return d


def largest_component(density, seeds):
    """Оставляет только связный кусок структуры, содержащий точки seeds.

    Если при резьбе от балки «отвалился» изолированный остров материала —
    он бесполезен (висит в воздухе) и должен исчезнуть. Обход в ширину
    (BFS) от опорных точек по 4-соседству помечает всё достижимое,
    остальное обнуляется.
    """
    keep = np.zeros_like(density, dtype=bool)
    q = collections.deque(s for s in seeds if density[s])
    for s in q:
        keep[s] = True
    while q:
        x, y = q.popleft()
        for dx, dy in ((1, 0), (-1, 0), (0, 1), (0, -1)):
            nx_, ny_ = x + dx, y + dy
            if 0 <= nx_ < NX and 0 <= ny_ < NY and density[nx_, ny_] \
                    and not keep[nx_, ny_]:
                keep[nx_, ny_] = True
                q.append((nx_, ny_))
    return density * keep


class CarveEnv:
    """Среда эпизода «резьбы»: за NUM_STEPS шагов выесть лишний материал.

    Не наследует gym.Env — интерфейс минимальный (reset/step), потому что
    обучающий скрипт topo_train.py написан вручную под неё.
    """

    def __init__(self, rng):
        self.rng = rng   # генератор случайных чисел (для сэмплинга удаляемых клеток)

    def reset(self):
        """Начало эпизода: сплошная балка, считаем её исходную жёсткость."""
        self.density = np.ones((NX, NY))
        self.step_i = 0
        # c0 — податливость сплошной балки; итоговая награда сравнивает с ней.
        self.c0, self.energy = solve(self.density)
        return self._obs()

    def _obs(self):
        """Наблюдение для агента: (доля оставшегося материала, прогресс эпизода).
        Больше агенту знать и не нужно — стратегия резьбы зависит только
        от того, сколько уже вырезано и сколько шагов осталось."""
        return np.array([self.density.mean(), self.step_i / NUM_STEPS])

    def step(self, frac, temp):
        """Один шаг резьбы.

        Вход: frac — какую долю активных клеток удалить на этом шаге;
              temp — температура софтмакса: 0 = резать строго самые
              ненагруженные, больше = добавить случайности.
        Выход: (наблюдение, награда, done, info). Награда не нулевая
        только на последнем шаге (задача с отложенной наградой).
        """
        d = self.density
        # Карта нагруженности с прошлого расчёта, сглаженная против
        # «шахматной доски».
        sens = blur(self.energy, radius=2, sigma=1.0)
        active = (d > 0.5) & ~PASSIVE          # что вообще можно резать
        n_remove = int(frac * active.sum())

        # Не даём вырезать больше целевой доли материала: если после
        # удаления объём упал бы ниже TARGET_VOL — урезаем аппетит.
        vol_after = (active.sum() - n_remove + PASSIVE.sum()) / d.size
        if vol_after < TARGET_VOL:
            n_remove = max(0, int((self.density.mean() - TARGET_VOL) * d.size))

        # Резать разрешено только слабонагруженную часть клеток (нижние
        # 60% по нагруженности): сильно нагруженные — несущий «скелет»,
        # его выедание мгновенно разрушило бы структуру.
        if active.any():
            cutoff = np.quantile(sens[active], 0.6)
            eligible = active & (sens <= cutoff)
        else:
            eligible = active
        n_remove = min(n_remove, int(eligible.sum()))

        if n_remove > 0:
            # Выбираем конкретные клетки случайно, но с перекосом:
            # чем слабее нагружена клетка, тем вероятнее её удаление.
            # temp управляет резкостью этого перекоса (softmax).
            idx = np.argwhere(eligible)
            e = sens[eligible]
            e = e / (e.mean() + 1e-12)          # нормировка масштаба
            logits = -e / max(temp, 1e-3)       # минус: слабее нагружено — вероятнее
            p = np.exp(logits - logits.max())   # численно устойчивый softmax
            p /= p.sum()
            chosen = self.rng.choice(len(idx), size=min(n_remove, len(idx)),
                                     replace=False, p=p)
            d[idx[chosen, 0], idx[chosen, 1]] = 0.0

        # Санитария после резьбы: убираем отвалившиеся куски (связность
        # от точки груза и от опоры) и «волоски».
        seeds = [(0, NY - 1), (NX - 1, 0)]
        self.density = prune_hairs(largest_component(d, seeds))

        self.step_i += 1
        done = self.step_i >= NUM_STEPS
        # Пересчитываем МКЭ — свежая карта нагруженности для следующего шага.
        c, self.energy = solve(self.density)

        reward = 0.0
        if done:
            # Финальная награда: минус относительная податливость
            # (c/c0 = 1 у сплошной балки, больше = хуже; обрезаем 30, чтобы
            # развалившиеся структуры не взрывали градиент) минус штраф
            # за лишний материал сверх цели (+2% допуска).
            vol = self.density.mean()
            reward = -min(c / self.c0, 30.0) \
                - 10.0 * max(0.0, vol - (TARGET_VOL + 0.02))
        return self._obs(), reward, done, {"compliance": c,
                                           "vol": self.density.mean()}
