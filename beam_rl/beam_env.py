"""RL-среда: проектирование двутавровой балки под 3D-печать (PLA).

ЧТО ЗДЕСЬ ПРОИСХОДИТ (для тех, кто не знаком с RL и сопроматом)
---------------------------------------------------------------
Это задача оптимизации, оформленная как среда обучения с подкреплением:
агент выдаёт числа (параметры балки), среда считает физику и возвращает
награду — одно число «насколько дизайн хорош». Агент учится выдавать
такие числа, чтобы награда была максимальной.

ПОСТАНОВКА ЗАДАЧИ: балка длиной SPAN_MM лежит на двух опорах, в центр
кладут груз DESIGN_LOAD_N (это называется «трёхточечный изгиб» — две
точки опоры + одна точка нагрузки). Сечение балки — двутавр (в разрезе
буква «I»: две горизонтальные полки, соединённые вертикальной стенкой).

Агент одним действием выбирает 4 параметра сечения:
  H  — высота балки,
  B  — ширина полки,
  tf — толщина полки,
  tw — толщина стенки.

НАГРАДА: минимальная масса при выполнении жёстких ограничений:
  * напряжение <= допускаемого для PLA (с запасом прочности x3),
  * прогиб (насколько балка провисает под грузом) <= лимита,
  * толщины стенок пригодны для печати соплом 0.4 мм.

Задача одношаговая («бандит»): одно действие = один готовый дизайн,
никакой последовательности шагов нет.
"""

import numpy as np
import gymnasium as gym
from gymnasium import spaces

# ---- Техзадание -----------------------------------------------------------
SPAN_MM = 150.0          # расстояние между опорами, мм (длина балки)
DESIGN_LOAD_N = 400.0    # расчётная нагрузка в центре, ньютоны (~40 кг)

# ---- Материал: PLA (стандартный пластик для 3D-печати) ---------------------
E_MPA = 3300.0           # модуль упругости (жёсткость материала), МПа
SIGMA_ULT_MPA = 50.0     # предел прочности на растяжение — ломается выше этого
SAFETY_FACTOR = 3.0      # запас прочности: печать анизотропна (слои слабее),
                         # плюс PLA «ползёт» под длительной нагрузкой
SIGMA_ALLOW_MPA = SIGMA_ULT_MPA / SAFETY_FACTOR   # допускаемое напряжение ~16.7 МПа
DENSITY_G_MM3 = 1.24e-3  # плотность PLA: 1.24 г/см^3, переведено в г/мм^3

DEFLECTION_LIMIT_MM = 1.0   # максимально допустимый прогиб в центре, мм

# ---- Границы параметров сечения (мм): H, B, tf, tw ------------------------
# H — высота балки, B — ширина полки, tf — толщина полки, tw — толщина стенки.
# Нижняя граница толщин 1.6 мм = 4 периметра сопла 0.4 мм — тоньше принтер
# не напечатает надёжно.
PARAM_LOW = np.array([10.0, 10.0, 1.6, 1.6])
PARAM_HIGH = np.array([32.0, 40.0, 6.0, 6.0])


def action_to_params(action):
    """Превращает «сырое» действие агента в физические размеры сечения.

    Агент выдаёт 4 произвольных числа без границ; tanh сжимает их в
    (-1, 1), затем линейно растягиваем в диапазоны PARAM_LOW..PARAM_HIGH.
    Работает батчами: можно подать (256, 4) — 256 дизайнов за раз.

    Вход:  action (..., 4) — сырые числа агента.
    Выход: params (..., 4) — H, B, tf, tw в миллиметрах.
    """
    a = np.tanh(np.asarray(action, dtype=np.float64))       # -> (-1, 1)
    p = PARAM_LOW + (a + 1.0) * 0.5 * (PARAM_HIGH - PARAM_LOW)  # -> физ. диапазоны

    # Геометрическая согласованность (иначе может получиться бессмыслица):
    # две полки не должны съедать всю высоту (оставляем >= 4 мм на стенку),
    # стенка не может быть шире полки.
    H, B = p[..., 0], p[..., 1]
    p[..., 2] = np.minimum(p[..., 2], (H - 4.0) / 2.0)
    p[..., 3] = np.minimum(p[..., 3], B)
    return p


def analyze(params):
    """Сопромат: считает напряжение, прогиб и массу двутавра (батчами).

    Все формулы — классика из учебника по сопротивлению материалов для
    балки на двух опорах с грузом посередине:
      * момент инерции I — «геометрическая жёсткость» сечения;
      * напряжение sigma = M / W — чем больше, тем ближе к поломке;
      * прогиб = P*L^3 / (48*E*I) — насколько провиснет центр.

    Вход:  params (..., 4) = H, B, tf, tw в мм.
    Выход: (sigma МПа, deflection мм, mass_g г) — каждая с формой батча.
    """
    H, B, tf, tw = (params[..., i] for i in range(4))
    hw = H - 2.0 * tf                                   # высота стенки (между полками)

    # Момент инерции двутавра: полный прямоугольник минус два «выреза»
    # по бокам от стенки. Чем больше I, тем жёстче балка.
    I = (B * H**3 - (B - tw) * hw**3) / 12.0            # мм^4

    W = I / (H / 2.0)                                   # момент сопротивления, мм^3
    M = DESIGN_LOAD_N * SPAN_MM / 4.0                   # макс. изгибающий момент
                                                        # (в центре балки), Н*мм
    sigma = M / W                                       # макс. напряжение, МПа
    deflection = DESIGN_LOAD_N * SPAN_MM**3 / (48.0 * E_MPA * I)  # прогиб центра, мм

    area = 2.0 * B * tf + tw * hw                       # площадь сечения, мм^2
    mass_g = area * SPAN_MM * DENSITY_G_MM3             # масса = площадь * длина * плотность
    return sigma, deflection, mass_g


def evaluate(params):
    """Считает награду для батча дизайнов.

    Логика награды: базовые 5 очков, минус масса (легче = лучше), минус
    большие штрафы за превышение прочности или прогиба. Коэффициент 40
    делает штрафы намного больнее выгоды от экономии массы — агенту
    невыгодно жульничать, нарушая ограничения.

    Вход:  params (..., 4) — размеры сечений, мм.
    Выход: (reward, feasible, sigma, deflection, mass_g), всё батчами;
           feasible — bool «все ограничения выполнены».
    """
    sigma, deflection, mass_g = analyze(params)
    # Насколько превышены лимиты (0 = не превышены):
    over_stress = np.maximum(0.0, sigma / SIGMA_ALLOW_MPA - 1.0)
    over_defl = np.maximum(0.0, deflection / DEFLECTION_LIMIT_MM - 1.0)
    reward = 5.0 - mass_g / 20.0 - 40.0 * over_stress - 40.0 * over_defl
    feasible = (over_stress == 0.0) & (over_defl == 0.0)
    return reward, feasible, sigma, deflection, mass_g


class BeamEnv(gym.Env):
    """Одношаговая (bandit) среда в стандартном интерфейсе gymnasium.

    Обёртка над evaluate() для совместимости с библиотеками RL
    (stable-baselines3 и т.п.). Обучающий скрипт train.py работает с
    evaluate() напрямую — так быстрее, — но эта обёртка позволяет
    подключить любой готовый алгоритм.

    Интерфейс gymnasium: reset() начинает эпизод и возвращает наблюдение;
    step(action) применяет действие и возвращает
    (наблюдение, награду, terminated, truncated, info).
    Наблюдение здесь фиктивное (нули) — в одношаговой задаче агенту
    нечего наблюдать, всё решает одно действие.
    """

    def __init__(self):
        # Пространства действий/наблюдений — обязательные атрибуты gym.Env.
        self.observation_space = spaces.Box(-1.0, 1.0, shape=(1,), dtype=np.float32)
        self.action_space = spaces.Box(-1.0, 1.0, shape=(4,), dtype=np.float32)

    def reset(self, *, seed=None, options=None):
        """Начало эпизода: возвращает фиктивное наблюдение и пустой info."""
        super().reset(seed=seed)
        return np.zeros(1, dtype=np.float32), {}

    def step(self, action):
        """Один шаг = весь эпизод: действие -> дизайн -> награда -> конец.

        Возвращает кортеж gymnasium:
          (наблюдение, награда, terminated=True (эпизод всегда кончается
          сразу), truncated=False, info с метриками дизайна).
        """
        params = action_to_params(action)
        reward, feasible, sigma, deflection, mass_g = evaluate(params)
        info = {
            "params_mm": params,
            "sigma_mpa": float(sigma),
            "deflection_mm": float(deflection),
            "mass_g": float(mass_g),
            "feasible": bool(feasible),
        }
        return np.zeros(1, dtype=np.float32), float(reward), True, False, info
