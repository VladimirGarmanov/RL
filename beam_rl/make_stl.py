"""Генерация STL двутавровой балки из best_beam.json.

ЧТО ДЕЛАЕТ ЭТОТ ФАЙЛ
--------------------
Берёт 4 размера сечения, найденные RL-агентом (train.py), и строит
печатаемую 3D-модель в формате STL (стандартный формат 3D-печати —
просто список треугольников в бинарном виде).

Балка экспортируется сразу в ориентации для печати: лежит на боку
(H-профилем), плоские кромки полок на столе, слои принтера идут вдоль
длины балки — это даёт максимальную прочность на изгиб (нагрузка не
отрывает слои друг от друга). Стенка печатается «мостом» между полками
(пролёт ~H-2tf мм, любой принтер справится).

Меш строится вручную: ушная триангуляция профиля для торцов + боковые
стенки. Затем проверки: водонепроницаемость (каждое ребро принадлежит
ровно 2 треугольникам — нет дыр) и объём (должен совпасть с
площадь сечения * длину — контроль корректности геометрии).

Запуск: python3 make_stl.py  (нужен best_beam.json от train.py)
"""

import json
import pathlib
import struct

import numpy as np

from beam_env import SPAN_MM, DENSITY_G_MM3


def i_profile(H, B, tf, tw):
    """Контур двутавра («I» в разрезе) в 2D, 12 вершин.

    Оси: a = ширина (горизонталь), b = высота. Обход против часовой
    стрелки (CCW) — это важно для триангуляции и ориентации нормалей.
    Порядок: нижняя полка -> правая сторона стенки -> верхняя полка ->
    левая сторона стенки.

    Вход:  H — высота, B — ширина полки, tf — толщина полки,
           tw — толщина стенки (все в мм).
    Выход: массив (12, 2) вершин контура.
    """
    return np.array([
        (-B / 2, 0), (B / 2, 0), (B / 2, tf), (tw / 2, tf),
        (tw / 2, H - tf), (B / 2, H - tf), (B / 2, H), (-B / 2, H),
        (-B / 2, H - tf), (-tw / 2, H - tf), (-tw / 2, tf), (-B / 2, tf),
    ], dtype=np.float64)


def triangulate(poly):
    """Разбивает плоский многоугольник на треугольники («ушная» триангуляция).

    Алгоритм ear clipping: ищем «ухо» — выпуклую вершину, чей треугольник
    не содержит других вершин контура, — отрезаем его, повторяем, пока не
    останется последний треугольник. Работает для любых простых (без
    самопересечений) многоугольников, включая невыпуклый двутавр.

    Вход:  poly (M, 2) — вершины контура с обходом CCW.
    Выход: список троек ИНДЕКСОВ вершин (не координат).
    """
    idx = list(range(len(poly)))   # индексы ещё не отрезанных вершин
    tris = []

    def cross(o, a, b):
        """Векторное произведение (o->a) x (o->b): > 0 = поворот против часовой."""
        return (a[0] - o[0]) * (b[1] - o[1]) - (a[1] - o[1]) * (b[0] - o[0])

    def point_in_tri(p, a, b, c):
        """Лежит ли точка p внутри треугольника abc (все три поворота одного знака)."""
        d1, d2, d3 = cross(a, b, p), cross(b, c, p), cross(c, a, p)
        return d1 >= -1e-9 and d2 >= -1e-9 and d3 >= -1e-9

    guard = 0   # предохранитель от бесконечного цикла на кривой геометрии
    while len(idx) > 3 and guard < 10000:
        guard += 1
        n = len(idx)
        for k in range(n):
            # Тройка соседних вершин — кандидат в «ухо».
            i0, i1, i2 = idx[k - 1], idx[k], idx[(k + 1) % n]
            a, b, c = poly[i0], poly[i1], poly[i2]
            if cross(a, b, c) <= 1e-9:           # вогнутая (рефлексная) вершина — не ухо
                continue
            if any(point_in_tri(poly[j], a, b, c)
                   for j in idx if j not in (i0, i1, i2)):
                continue                          # внутри есть другая вершина — не ухо
            tris.append((i0, i1, i2))             # отрезаем ухо
            idx.pop(k)
            break
    tris.append(tuple(idx))   # последние 3 вершины — финальный треугольник
    return tris


def build_mesh(H, B, tf, tw, L):
    """Строит полный 3D-меш балки (два торца + боковые стенки).

    Ориентация под печать: X — длина балки, Y — высота профиля H,
    Z — ширина полки B (вертикаль принтера; балка лежит на кромках полок).

    Выход: массив треугольников (N, 3, 3) — N треугольников по 3 вершины
    по 3 координаты.
    """
    prof = i_profile(H, B, tf, tw)
    n = len(prof)
    cap = triangulate(prof)   # триангуляция профиля — для торцевых крышек

    def v(x, p):
        """Точка 2D-профиля -> 3D: a(ширина) -> Z (поднято на B/2, чтобы
        модель лежала на столе, z >= 0), b(высота) -> Y."""
        return (x, p[1], p[0] + B / 2)

    tris = []
    # Торцы. Нюанс ориентации: отображение (a,b)->(Z,Y) меняет хиральность
    # (это зеркало), поэтому CCW-профиль при взгляде с +X выглядит CW —
    # прямой порядок вершин даёт нормаль в -X, ей закрываем торец x=0;
    # у торца x=L порядок обратный (нормаль +X, наружу).
    for (i, j, k) in cap:
        tris.append((v(0, prof[i]), v(0, prof[j]), v(0, prof[k])))
        tris.append((v(L, prof[i]), v(L, prof[k]), v(L, prof[j])))
    # Боковая поверхность: каждое ребро профиля протягивается вдоль X
    # в прямоугольник = 2 треугольника.
    for i in range(n):
        j = (i + 1) % n
        a0, b0 = v(0, prof[i]), v(0, prof[j])
        a1, b1 = v(L, prof[i]), v(L, prof[j])
        tris.append((a0, b1, b0))
        tris.append((a0, a1, b1))
    return np.array(tris, dtype=np.float64)


def check_mesh(tris, expected_volume):
    """Проверяет пригодность меша для 3D-печати.

    Три условия:
      1. Водонепроницаемость: каждое ребро принадлежит РОВНО двум
         треугольникам (1 = дыра, 3+ = самопересечение).
      2. Объём > 0: нормали смотрят наружу.
      3. Объём совпадает с ожидаемым (площадь сечения * длина) —
         страховка от ошибок построения.

    Падает с assert и понятным сообщением, если что-то не так.
    Возвращает объём, мм^3.
    """
    edges = {}
    for t in tris:
        for e in ((0, 1), (1, 2), (2, 0)):
            # Ключ ребра — отсортированная пара вершин, чтобы (A,B) и (B,A)
            # считались одним ребром.
            key = tuple(sorted((tuple(t[e[0]]), tuple(t[e[1]]))))
            edges[key] = edges.get(key, 0) + 1
    bad = [k for k, c in edges.items() if c != 2]
    assert not bad, f"меш не водонепроницаем: {len(bad)} рёбер"

    # Объём замкнутой оболочки: сумма смешанных произведений вершин / 6
    # (формула дивергенции). Знак говорит об ориентации нормалей.
    vol = sum(np.dot(t[0], np.cross(t[1], t[2])) for t in tris) / 6.0
    assert vol > 0, "нормали направлены внутрь"
    assert abs(vol - expected_volume) / expected_volume < 1e-6, \
        f"объём {vol:.1f} != ожидаемого {expected_volume:.1f}"
    return vol


def write_stl(path, tris):
    """Пишет треугольники в бинарный STL-файл.

    Формат прост: 80 байт заголовка, 4 байта — число треугольников,
    затем на каждый треугольник: нормаль (3 float) + 3 вершины (9 float)
    + 2 байта атрибутов. struct.pack("<...") — упаковка в little-endian.
    """
    with open(path, "wb") as f:
        f.write(b"RL-optimized PLA I-beam".ljust(80, b"\0"))
        f.write(struct.pack("<I", len(tris)))
        for t in tris:
            # Нормаль = нормированное векторное произведение двух сторон.
            nrm = np.cross(t[1] - t[0], t[2] - t[0])
            ln = np.linalg.norm(nrm)
            nrm = nrm / ln if ln > 0 else nrm
            f.write(struct.pack("<3f", *nrm))
            for p in t:
                f.write(struct.pack("<3f", *p))
            f.write(struct.pack("<H", 0))


if __name__ == "__main__":
    # Читаем размеры лучшей балки, найденной train.py.
    here = pathlib.Path(__file__).parent
    best = json.loads((here / "best_beam.json").read_text())
    H, B, tf, tw = best["H_mm"], best["B_mm"], best["tf_mm"], best["tw_mm"]

    # Строим меш и проверяем его (объём должен сойтись с аналитическим).
    tris = build_mesh(H, B, tf, tw, SPAN_MM)
    area = 2 * B * tf + tw * (H - 2 * tf)   # площадь сечения двутавра
    vol = check_mesh(tris, area * SPAN_MM)

    out = here / "beam.stl"
    write_stl(out, tris)

    # Сводка: габариты, масса, характеристики из JSON.
    lo, hi = tris.reshape(-1, 3).min(axis=0), tris.reshape(-1, 3).max(axis=0)
    print(f"STL: {out}")
    print(f"Треугольников: {len(tris)}, меш водонепроницаем, объём {vol/1000:.1f} см3")
    print(f"Габариты XYZ: {hi[0]-lo[0]:.1f} x {hi[1]-lo[1]:.1f} x {hi[2]-lo[2]:.1f} мм")
    print(f"Масса (100% PLA): {vol * DENSITY_G_MM3:.1f} г")
    print(f"Сечение: H={H} B={B} tf={tf} tw={tw} мм | "
          f"sigma={best['sigma_mpa']} МПа, прогиб={best['deflection_mm']} мм, "
          f"запас прочности x{best['safety_factor']}")
