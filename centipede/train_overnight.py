#!/usr/bin/env python
"""
Обучение универсальной модели МАКСИМУМ плавности:
180 млн шагов в командном режиме (примерно 80-100 часов на 8-ядерном CPU).

Запуск:
    python train_overnight.py           — обучить с нуля (на неделю)
    python train_overnight.py --resume  — продолжить сохранённый чекпоинт

После завершения:
    python play.py                      — смотреть результат, максимально плавная ходьба
    python play.py --auto               — автопрограмма команд

ВНИМАНИЕ: это ДОЛГО. 180М шагов = ~100 часов на CPU. Если есть GPU — используйте её.
Или запустите на сервере / в облаке с более мощным процессором.
"""

import sys
import subprocess

if __name__ == "__main__":
    # Переопределяем параметры train.py для максимального обучения
    resume = "--resume" in sys.argv

    # Одна универсальная сеть, командный режим, МАКСИМУМ плавности
    cmd = [
        sys.executable, "train.py",
        "--reward-mode", "command",
        "--steps", "180000000",  # 180М шагов для СУПЕР плавности и баланса
    ]

    if resume:
        cmd.append("--resume")

    print("=" * 80)
    print("ОБУЧЕНИЕ УНИВЕРСАЛЬНОЙ МОДЕЛИ — МАКСИМУМ ПЛАВНОСТИ И БАЛАНСА")
    print("=" * 80)
    print(f"Режим: {'resume' if resume else 'fresh start'}")
    print(f"Шагов: 180,000,000 (примерно 80-100 часов на CPU с 8 ядрами)")
    print(f"Сеть: [768, 512], батч 2048, rollout 32768")
    print(f"Плавность: lr=5e-5, clip=0.1, max_grad=0.3 (супер консервативно)")
    print(f"Штрафы: action_rate=0.20 (было 0.05), dof_vel=5e-4 (было 1e-4)")
    print(f"Результат: models/ppo_centipede_final.zip")
    print()
    print("После обучения робот будет:")
    print("  ✓ Очень плавно ходить (никаких рывков)")
    print("  ✓ Активно балансировать даже на месте")
    print("  ✓ Реагировать на все стрелки: вперёд↑ назад↓ влево← вправо→ стоп")
    print()
    print("РЕКОМЕНДАЦИЯ: запустите в экране (screen / tmux) или в фоне (&)")
    print("=" * 80)
    print()

    sys.exit(subprocess.call(cmd))
