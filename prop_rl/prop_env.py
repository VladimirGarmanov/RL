"""RL-среда: проектирование тихого 13-дюймового воздушного винта (пропеллера).

ЧТО ЗДЕСЬ ПРОИСХОДИТ (для тех, кто не знаком с RL и аэродинамикой)
------------------------------------------------------------------
Это НЕ игра, а задача оптимизации, оформленная как среда для обучения
с подкреплением (Reinforcement Learning, RL). Идея RL простая:

  1. Агент (нейросеть или просто распределение вероятностей) выдаёт
     "действие" — набор чисел.
  2. Среда (этот файл) превращает действие в конкретный дизайн винта,
     считает его физику и возвращает "награду" — одно число, которое
     говорит, насколько дизайн хорош.
  3. Агент подстраивает свои параметры так, чтобы награда росла.

Здесь задача одношаговая (в RL это называют "бандитом", от "однорукого
бандита" — игрового автомата): один выстрел действием = один готовый
дизайн, никакой последовательности шагов нет.

ЧТО ЗАДАЁТ АГЕНТ (9 чисел в действии):
  * хорду лопасти (ширину профиля) в 4 контрольных сечениях по радиусу,
  * угол установки (крутку) в тех же 4 сечениях,
  * рабочие обороты (RPM).

Винт вписан в цилиндр диаметром 13 дюймов (330 мм).

ФИЗИКА — классическая BEMT (Blade Element + Momentum Theory) для статики
(взлёт/висение): лопасть мысленно режется на кольцевые элементы, для
каждого итерационно согласуются подъёмная сила профиля и индуктивная
скорость (скорость воздуха, который винт "подсасывает" сверху) через
баланс импульса, с концевыми потерями Прандтля (у самого конца лопасти
воздух перетекает через кромку и тяга там падает).

ШУМ — инженерный прокси (упрощённая оценка): широкополосный (вихревой)
шум растёт как ~V_tip^6 (скорость на 75% радиуса в 6-й степени!) плюс
член нагрузки. Абсолютные децибелы условны, но градиент верный:
медленный широкий винт с большой круткой тише быстрого узкого при той
же тяге.

НАГРАДА: минимум шума при тяге >= TARGET_THRUST_N, мощности <= лимита
и запасе до срыва потока (срыв = крыло/лопасть "перетянули" по углу
атаки, подъёмная сила резко падает).
"""

import numpy as np

# ---- Техзадание -------------------------------------------------------------
# Жёсткие требования к винту, которые не меняются во время обучения.

DIAMETER_IN = 13.0                       # диаметр винта в дюймах
R = DIAMETER_IN * 25.4 / 2 / 1000        # радиус в метрах: 13" -> 0.1651 м
R_HUB = 0.018                            # м, радиус ступицы — до него лопасти нет,
                                         # рабочая часть лопасти начинается отсюда
N_BLADES = 2                             # количество лопастей
TARGET_THRUST_N = 12.0                   # требуемая тяга, ньютоны (~1.2 кгс на взлёте)
POWER_LIMIT_W = 300.0                    # лимит мощности мотора, ватты
RHO = 1.225                              # плотность воздуха, кг/м3 (уровень моря)

# ---- Аэродинамика профиля (Clark-Y-подобный, Re ~1e5) ------------------------
# Профиль — это форма поперечного сечения лопасти (как крыло самолёта в разрезе).
# Ниже — его аэродинамические коэффициенты:
#   Cl (lift)  — коэффициент подъёмной силы, растёт с углом атаки;
#   Cd (drag)  — коэффициент сопротивления.

CL_ALPHA = 2 * np.pi * 0.9               # наклон кривой Cl(alpha), 1/радиан.
                                          # 2*pi — теория тонкого профиля,
                                          # 0.9 — поправка на конечную толщину/вязкость
ALPHA0 = np.deg2rad(-4.0)                 # угол атаки, при котором подъёмная сила = 0.
                                          # Отрицательный, потому что профиль изогнутый
                                          # (он "несёт" даже при нулевом угле)
CL_MAX = 1.25                             # максимальный Cl — больше профиль физически не даст
CD0 = 0.015                               # сопротивление при нулевой подъёмной силе
K_IND = 0.045                             # индуктивная добавка: Cd = CD0 + K*Cl^2
                                          # (чем больше несёшь, тем больше сопротивление)
ALPHA_STALL = np.deg2rad(11.0)            # угол атаки, после которого начинается срыв потока

# ---- Дискретизация лопасти ---------------------------------------------------
# Лопасть режем на N_ELEM кольцевых элементов — для каждого физика считается
# отдельно, потом всё суммируется. Это и есть "blade element" в BEMT.

N_ELEM = 30                               # количество элементов
R_EL = np.linspace(R_HUB, R, N_ELEM + 1)  # границы элементов по радиусу (31 точка)
R_MID = 0.5 * (R_EL[:-1] + R_EL[1:])      # центры элементов (30 точек) — здесь считаем силы
DR = np.diff(R_EL)                        # ширина каждого элемента, м

# Контрольные станции: агент задаёт хорду/крутку только в 4 точках
# (на 25%, 50%, 75% и 100% радиуса), остальное интерполируется линейно.
STATIONS = np.array([0.25, 0.5, 0.75, 1.0])

# Физически осмысленные границы параметров. Агент выдаёт числа без границ,
# а мы (в action_to_params) сжимаем их в эти диапазоны:
CHORD_LOW, CHORD_HIGH = 0.014, 0.042              # хорда 14..42 мм
TWIST_LOW, TWIST_HIGH = np.deg2rad(2.0), np.deg2rad(38.0)  # крутка 2..38 градусов
RPM_LOW, RPM_HIGH = 2000.0, 6500.0                # обороты 2000..6500 об/мин


def action_to_params(action):
    """Превращает "сырое" действие агента в физические параметры винта.

    Агент выдаёт 9 произвольных чисел (может и -50, и +100 — он не знает
    про физические границы). Наша задача — гладко и обратимо сжать их
    в допустимые диапазоны.

    Вход:  action — массив формы (..., 9); работает и с батчами
           (т.е. можно подать сразу (96, 9) — 96 дизайнов за раз).
    Выход: (chord[..., 4] в метрах, twist[..., 4] в радианах, rpm[...]).
    """
    # Шаг 1: tanh сжимает любое число в диапазон (-1, 1).
    # Это стандартный приём в RL для ограниченных действий.
    a = np.tanh(np.asarray(action, dtype=np.float64))

    # Шаг 2: переводим (-1, 1) -> (0, 1), чтобы удобно масштабировать.
    u = (a + 1.0) * 0.5

    # Шаг 3: линейно растягиваем (0,1) в физические диапазоны.
    # Первые 4 числа действия -> хорды, следующие 4 -> крутки, последнее -> RPM.
    chord = CHORD_LOW + u[..., 0:4] * (CHORD_HIGH - CHORD_LOW)
    twist = TWIST_LOW + u[..., 4:8] * (TWIST_HIGH - TWIST_LOW)

    # Крутку принудительно сортируем по убыванию от комля (основания) к концу:
    # у реальных винтов угол установки всегда уменьшается к кончику лопасти
    # (потому что окружная скорость там выше). Трюк "-sort(-x)" = сортировка
    # по убыванию, потому что np.sort умеет только по возрастанию.
    twist = -np.sort(-twist, axis=-1)

    rpm = RPM_LOW + u[..., 8] * (RPM_HIGH - RPM_LOW)
    return chord, twist, rpm


def interp_blade(chord_st, twist_st):
    """Строит полные распределения хорды и крутки по всем 30 элементам лопасти.

    Агент задал значения только в 4 контрольных точках (STATIONS);
    здесь линейной интерполяцией (np.interp) заполняем промежутки.

    Вход:  chord_st (4,) — хорды в контрольных точках, м;
           twist_st (4,) — крутки в контрольных точках, рад.
    Выход: (chord (30,), twist (30,)) — значения в центрах всех элементов.
    """
    x = R_MID / R                          # относительный радиус центров элементов (0..1)
    chord = np.interp(x, STATIONS, chord_st)
    twist = np.interp(x, STATIONS, twist_st)
    return chord, twist


def bemt(chord, beta, rpm):
    """Считает тягу и мощность винта методом BEMT (главная физика проекта).

    Идея метода: каждый кольцевой элемент лопасти — как маленькое крыло.
    Но угол атаки элемента зависит от того, какую скорость воздуха винт
    сам через себя прогоняет ("индуктивная скорость" v). А v зависит от
    тяги, которая зависит от угла атаки... Замкнутый круг — поэтому
    решаем итерациями: угадали v -> посчитали силы -> уточнили v -> и
    так 80 раз, пока не сойдётся.

    Вход:  chord (30,) — хорда каждого элемента, м;
           beta  (30,) — угол установки каждого элемента, рад;
           rpm         — обороты, об/мин.
    Выход: thrust (Н), power (Вт), alpha (рад, углы атаки по элементам),
           v_tip (м/с, скорость конца лопасти), blade_area (м2, одна лопасть).
    """
    omega = rpm * 2 * np.pi / 60.0         # об/мин -> угловая скорость, рад/с
    ut = omega * R_MID                     # окружная скорость каждого элемента, м/с
                                           # (чем дальше от оси, тем быстрее)

    # Стартовое приближение индуктивной скорости: ~5% окружной + 1 м/с.
    # Точное значение не важно — итерации всё равно сойдутся.
    v = 0.05 * ut + 1.0

    for _ in range(80):                    # 80 итераций хватает для сходимости
        # Полная скорость потока, набегающего на элемент (Пифагор:
        # окружная ut + осевая v перпендикулярны).
        w2 = ut**2 + v**2

        # phi — угол притекания потока: под каким углом воздух реально
        # набегает на элемент (из-за индуктивной скорости — не строго вбок).
        phi = np.arctan2(v, ut)

        # Угол атаки = угол установки лопасти минус угол притекания.
        alpha = beta - phi

        # Коэффициент подъёмной силы: линейный рост с углом атаки,
        # обрезанный сверху CL_MAX (профиль не может нести бесконечно).
        cl = np.clip(CL_ALPHA * (alpha - ALPHA0), -0.3, CL_MAX)

        # Коэффициент сопротивления: базовый + индуктивный (растёт с Cl^2)
        # + штрафной член за срыв (резкий рост, если угол атаки за ALPHA_STALL).
        cd = CD0 + K_IND * cl**2 + 1.2 * np.maximum(0.0, alpha - ALPHA_STALL)**2

        # Погонная тяга элемента (Н/м): классическая формула
        # dT/dr = 0.5 * rho * (число лопастей) * хорда * V^2 * (проекция сил на ось).
        # Cl тянет вверх, Cd тянет назад — проецируем обе силы на ось винта.
        dt_dr = 0.5 * RHO * N_BLADES * chord * w2 * (cl * np.cos(phi)
                                                     - cd * np.sin(phi))

        # Концевые потери Прандтля: у самого кончика лопасти воздух
        # перетекает через кромку с нижней стороны на верхнюю, и тяга там
        # падает. Формула даёт множитель tip_loss в диапазоне (0..1),
        # который ~1 в середине лопасти и ->0 у кончика.
        f = N_BLADES / 2.0 * (R - R_MID) / (R_MID * np.sin(phi) + 1e-9)
        tip_loss = 2.0 / np.pi * np.arccos(np.clip(np.exp(-f), 0.0, 1.0))
        tip_loss = np.maximum(tip_loss, 0.05)   # не даём упасть до нуля (числ. устойчивость)

        # Баланс импульса ("momentum theory"): та же погонная тяга кольца
        # выражается через индуктивную скорость: dT = 4*pi*rho*r*v^2*F.
        # Отсюда выражаем "какой должна быть v при такой тяге".
        v_new = np.sqrt(np.maximum(dt_dr, 0.0)
                        / (4 * np.pi * RHO * R_MID * tip_loss))

        # Обновляем v не сразу, а с релаксацией (60% старого + 40% нового) —
        # иначе итерации могут скакать туда-сюда и не сойтись.
        v = 0.6 * v + 0.4 * v_new

    # Погонный крутящий момент (Н·м/м): те же силы, но проекция в плоскость
    # вращения (то, что моторy приходится "проворачивать"), умноженная на плечо R_MID.
    dq_dr = 0.5 * RHO * N_BLADES * chord * w2 * (cl * np.sin(phi)
                                                 + cd * np.cos(phi)) * R_MID

    # Интегрируем по лопасти: сумма (погонная величина * ширина элемента).
    thrust = float(np.sum(dt_dr * DR))            # полная тяга, Н
    power = float(np.sum(dq_dr * DR) * omega)     # мощность = момент * угловая скорость, Вт
    blade_area = float(np.sum(chord * DR))        # площадь одной лопасти в плане, м2
    return thrust, power, alpha, omega * R, blade_area


def noise_proxy(v_tip, blade_area, thrust):
    """Оценка шума винта в дБ (условный уровень на расстоянии 3 м).

    Это не точная акустика, а "прокси" — упрощённая формула, у которой
    правильное НАПРАВЛЕНИЕ (что делает винт тише/громче), но условные
    абсолютные значения.

    Два слагаемых:
      1. Вихревой (широкополосный) шум ~ 10*log10(A_лоп * V07^6):
         ШЕСТАЯ степень скорости — главный физический рычаг тишины.
         Снизил скорость конца лопасти вдвое = -18 дБ. Скорость берём
         на 75% радиуса (V07) — там "акустический центр" лопасти.
      2. Член нагрузки ~ 20*log10(T): при прочих равных больше тяги =
         больше шума (вес 0.3 — этот член второстепенный).

    Вход:  v_tip (м/с), blade_area (м2, одна лопасть), thrust (Н).
    Выход: уровень шума, дБ (float).
    """
    v07 = 0.7 * v_tip                       # скорость на ~75% радиуса
    # np.maximum(..., 1.0) защищает от log(0) при вырожденных дизайнах;
    # 3.4e-4 — калибровочная константа, подогнанная под реальные винты.
    spl_vortex = 10.0 * np.log10(N_BLADES * blade_area * np.maximum(v07, 1.0)**6
                                 * 3.4e-4)
    spl_load = 20.0 * np.log10(np.maximum(thrust, 0.1))
    return spl_vortex + 0.3 * spl_load


def evaluate_single(action, objective="quiet"):
    """Полный расчёт ОДНОГО дизайна: действие агента -> награда + все метрики.

    Это "сердце" среды: именно эту функцию агент косвенно максимизирует.

    Два режима (какой винт хотим получить):
      objective="quiet"  — минимум шума при тяге >= цели (взлётной);
      objective="thrust" — максимум тяги при мощности <= лимита.

    Вход:  action (9,) — сырое действие агента;
           objective   — строка "quiet" или "thrust".
    Выход: (reward, feasible, info):
      reward   — число-награда (больше = лучше), это сигнал для обучения;
      feasible — bool, выполнены ли ВСЕ жёсткие ограничения
                 (по нему отбираем лучший дизайн — награда может быть
                 высокой и у слегка нарушающего ограничения дизайна);
      info     — словарь со всеми метриками для логов и сохранения.
    """
    # Цепочка: действие -> параметры -> распределения по лопасти -> физика.
    chord_st, twist_st, rpm = action_to_params(action)
    chord, beta = interp_blade(chord_st, twist_st)
    thrust, power, alpha, v_tip, blade_area = bemt(chord, beta, rpm)
    spl = noise_proxy(v_tip, blade_area, thrust)

    # Метрики нарушения ограничений (все >= 0; 0 = ограничение выполнено):
    # доля элементов лопасти в срыве потока...
    stall_frac = float(np.mean(alpha > ALPHA_STALL))
    # ...недобор тяги в долях от цели (0 если тяги хватает)...
    thrust_deficit = max(0.0, 1.0 - thrust / TARGET_THRUST_N)
    # ...превышение мощности в долях от лимита (0 если в лимите).
    power_over = max(0.0, power / POWER_LIMIT_W - 1.0)

    if objective == "thrust":
        # Цель — тяга: награда растёт с тягой, штрафы за превышение
        # мощности и срыв. Коэффициенты (15, 8) подобраны так, чтобы
        # штрафы доминировали над выгодой от нарушения.
        reward = thrust / 2.0 - 15.0 * power_over - 8.0 * stall_frac
        feasible = (power_over == 0.0) and (stall_frac < 0.15)
    else:
        # Цель — тишина: награда растёт при снижении шума (минус перед spl),
        # штрафы за недобор тяги, перебор мощности и срыв.
        # "-60.0" и "/3.0" просто нормируют масштаб награды к удобным числам.
        reward = -(spl - 60.0) / 3.0 \
            - 25.0 * thrust_deficit - 10.0 * power_over - 8.0 * stall_frac
        feasible = (thrust_deficit == 0.0) and (power_over == 0.0) \
            and (stall_frac < 0.15)

    # Собираем все метрики в словарь — для печати прогресса и best_prop.json.
    return reward, feasible, {
        "thrust_n": thrust, "power_w": power, "rpm": float(rpm),
        "spl_db": float(spl), "v_tip": float(v_tip),
        "stall_frac": stall_frac,
        "chord_mm": (chord_st * 1000).round(1).tolist(),      # м -> мм для читаемости
        "twist_deg": np.rad2deg(twist_st).round(1).tolist(),  # рад -> градусы
        # эффективность в граммах тяги на ватт (популярная метрика у дроноводов)
        "efficiency_g_w": thrust / 9.81 * 1000 / max(power, 1e-6),
    }


def evaluate(actions, objective="quiet"):
    """Оценивает БАТЧ дизайнов сразу (нужно алгоритму REINFORCE в prop_train.py).

    RL-алгоритм на каждой итерации пробует сразу много (например 96)
    случайных вариантов действий — здесь мы просто прогоняем каждый через
    evaluate_single в цикле. (BEMT итерационный, поэтому векторизовать
    сложно — этот цикл медленнее, чем аналог в beam_rl, но терпимо.)

    Вход:  actions (N, 9) — батч действий; objective — режим.
    Выход: (rewards (N,), feasibles (N,) bool, infos — список словарей).
    """
    rewards = np.empty(len(actions))
    feasibles = np.empty(len(actions), dtype=bool)
    infos = []
    for i, a in enumerate(actions):
        rewards[i], feasibles[i], info = evaluate_single(a, objective)
        infos.append(info)
    return rewards, feasibles, infos
