"""Генерация 3D-модели винта для печати: best_prop.json -> prop_13in.stl.

ЧТО ДЕЛАЕТ ЭТОТ ФАЙЛ
--------------------
Берёт параметры лучшего винта, найденные RL-агентом (prop_train.py),
и строит из них полноценную 3D-модель в формате STL — стандартном
формате для 3D-печати (просто список треугольников в бинарном виде).

ГЕОМЕТРИЯ: две лопасти + ступица (центральная втулка).
  * Лопасть строится "лофтом" (loft — протяжка профиля вдоль пути):
    берём контур аэродинамического профиля NACA 4412 и ставим его в
    ~14 радиальных сечениях; в каждом сечении профиль масштабируется
    хордой, поворачивается на местный угол установки (крутку) вокруг
    оси на четверти хорды и ставится на свой радиус. Соседние сечения
    соединяются полосками треугольников.
  * Ступица — труба (наружный диаметр 32 мм, канал под вал 5 мм).

КОНТРОЛЬ КАЧЕСТВА: каждое тело (2 лопасти, ступица) — отдельная
замкнутая оболочка; проверяется водонепроницаемость (у каждого ребра
ровно 2 треугольника — нет дыр) и положительный объём (нормали наружу).
Оболочки пересекаются (корень лопасти утоплен в ступицу) — это нормально,
слайсер (программа подготовки к печати) сам объединит их в одно тело.

Запуск:
    python3 prop_stl.py                        # из best_prop.json
    python3 prop_stl.py best_prop_thrust.json  # тяговый вариант
"""

import json
import pathlib
import struct

import numpy as np

from prop_env import R, STATIONS

MM = 1000.0                      # внутри считаем в метрах, STL пишем в миллиметрах

# ---- Размеры ступицы ----------------------------------------------------------
HUB_R_OUT = 0.016                # наружный радиус ступицы, м (диаметр 32 мм)
HUB_R_BORE = 0.0025              # радиус канала под вал, м (вал 5 мм)
HUB_HALF_H = 0.006               # полувысота ступицы, м (полная высота 12 мм)
HUB_SEG = 48                     # на сколько сегментов делим окружность
                                 # (больше = глаже, но больше треугольников)

# ---- Дискретизация лопасти ----------------------------------------------------
N_AIRFOIL = 24                   # точек на одну поверхность профиля (верх/низ)
# Радиусы сечений лопасти: два первых внутри ступицы (чтобы корень
# гарантированно "утонул" в ней), дальше 12 сечений равномерно до кончика.
BLADE_STATIONS = np.concatenate([[0.010, 0.0135],
                                 np.linspace(0.018, R, 12)])


def naca4412(n=N_AIRFOIL):
    """Строит контур аэродинамического профиля NACA 4412 (хорда = 1).

    NACA 4412 — классический профиль с хорошей несущей способностью;
    цифры кодируют форму: изгиб 4%, положение изгиба 40%, толщина 12%.
    Формулы ниже — стандартные уравнения NACA-профилей из учебников.

    Выход: замкнутый контур (2n, 2) с обходом против часовой стрелки (CCW):
           задняя кромка (TE) -> верх -> передняя кромка (LE) -> низ -> TE.
    """
    m, p, t = 0.04, 0.4, 0.12    # изгиб, позиция изгиба, толщина (из "4412")

    # Косинусное распределение точек: сгущает точки к передней и задней
    # кромкам, где кривизна больше (равномерная сетка дала бы гранёный нос).
    x = 0.5 * (1 - np.cos(np.linspace(0, np.pi, n + 1)))

    # Средняя линия профиля (camber line) — два уравнения: до и после точки p.
    yc = np.where(x < p, m / p**2 * (2 * p * x - x**2),
                  m / (1 - p)**2 * ((1 - 2 * p) + 2 * p * x - x**2))
    # Её производная (наклон) — нужна, чтобы откладывать толщину
    # перпендикулярно средней линии, а не просто вертикально.
    dyc = np.where(x < p, 2 * m / p**2 * (p - x),
                   2 * m / (1 - p)**2 * (p - x))
    th = np.arctan(dyc)

    # Распределение полутолщины. Последний коэффициент -0.1036 (вместо
    # стандартного -0.1015) даёт закрытую заднюю кромку — важно для
    # водонепроницаемой 3D-модели.
    yt = 5 * t * (0.2969 * np.sqrt(x) - 0.1260 * x - 0.3516 * x**2
                  + 0.2843 * x**3 - 0.1036 * x**4)

    # Верхняя и нижняя поверхности: откладываем толщину перпендикулярно
    # средней линии (отсюда sin/cos).
    xu, zu = x - yt * np.sin(th), yc + yt * np.cos(th)
    xl, zl = x + yt * np.sin(th), yc - yt * np.cos(th)

    # Склеиваем в один замкнутый контур. Верх идём от TE к LE (поэтому
    # [::-1]), низ — от LE к TE; у нижней поверхности отбрасываем первую
    # и последнюю точки ([1:-1]), чтобы не дублировать кромки.
    upper = np.stack([xu[::-1], zu[::-1]], axis=1)          # TE -> LE
    lower = np.stack([xl[1:-1], zl[1:-1]], axis=1)          # LE -> TE, без концов
    return np.concatenate([upper, lower])                    # итог: (2n, 2)


# Контур считаем один раз при импорте — он одинаков для всех сечений.
AIRFOIL = naca4412()


def section_3d(r_pos, chord, beta):
    """Ставит контур профиля в 3D на заданный радиус лопасти.

    Три преобразования плоского контура (хорда 1) в реальное сечение:
      1. Масштаб: умножаем на хорду (реальную ширину профиля здесь).
      2. Крутка: поворачиваем на угол установки beta вокруг оси,
         проходящей через четверть хорды (стандартная ось у винтов —
         там аэродинамический фокус профиля, сечения не "гуляют").
      3. Установка: сдвигаем на радиус r_pos вдоль оси Y (лопасть растёт
         вдоль +Y).

    Вход:  r_pos (м) — радиус сечения; chord (м) — хорда; beta (рад) — крутка.
    Выход: точки сечения (M, 3) в координатах (x, y, z).
    """
    xc = (AIRFOIL[:, 0] - 0.25) * chord   # сдвиг: ось вращения — четверть хорды
    z = AIRFOIL[:, 1] * chord
    # Поворот на beta в плоскости (x, z); знак подобран так, чтобы
    # передняя кромка (LE) поднималась вверх при beta > 0.
    xr = xc * np.cos(beta) + z * np.sin(beta)
    zr = -xc * np.sin(beta) + z * np.cos(beta)
    pts = np.zeros((len(xc), 3))
    pts[:, 0], pts[:, 1], pts[:, 2] = xr, r_pos, zr
    return pts


def triangulate_2d(poly):
    """Разбивает плоский многоугольник на треугольники ("ушная" триангуляция).

    Нужно, чтобы закрыть торцы лопасти (корень и кончик) сплошными
    крышками. Алгоритм "ear clipping": ищем "ухо" — выпуклую вершину,
    треугольник при которой не содержит других вершин, — отрезаем его,
    повторяем, пока не останется один треугольник.

    (Копия функции из beam_rl — продублирована локально, чтобы не тянуть
    путь к соседнему проекту.)

    Вход:  poly (M, 2) — вершины многоугольника (обход CCW).
    Выход: список троек индексов вершин — треугольники.
    """
    idx = list(range(len(poly)))   # индексы ещё не отрезанных вершин
    tris = []

    def cross(o, a, b):
        """Векторное произведение (o->a) x (o->b): >0 = поворот против часовой."""
        return (a[0] - o[0]) * (b[1] - o[1]) - (a[1] - o[1]) * (b[0] - o[0])

    def inside(p, a, b, c):
        """Лежит ли точка p внутри треугольника abc (по знакам трёх поворотов)."""
        return (cross(a, b, p) >= -1e-12 and cross(b, c, p) >= -1e-12
                and cross(c, a, p) >= -1e-12)

    guard = 0   # предохранитель от бесконечного цикла на "плохой" геометрии
    while len(idx) > 3 and guard < 20000:
        guard += 1
        n = len(idx)
        for k in range(n):
            # Тройка соседних вершин — кандидат в "ухо".
            i0, i1, i2 = idx[k - 1], idx[k], idx[(k + 1) % n]
            a, b, c = poly[i0], poly[i1], poly[i2]
            if cross(a, b, c) <= 1e-12:
                continue   # вершина вогнутая (или вырожденная) — не ухо
            if any(inside(poly[j], a, b, c) for j in idx
                   if j not in (i0, i1, i2)):
                continue   # внутри треугольника есть другая вершина — не ухо
            tris.append((i0, i1, i2))   # отрезаем ухо
            idx.pop(k)
            break
    tris.append(tuple(idx))   # последние 3 вершины — финальный треугольник
    return tris


def blade_mesh(chord_st, twist_st):
    """Строит замкнутую треугольную оболочку одной лопасти (вдоль оси +Y).

    Вход:  chord_st (4,) — хорды в контрольных точках, м;
           twist_st (4,) — крутки в контрольных точках, рад.
    Выход: массив треугольников (T, 3, 3) с нормалями наружу.
    """
    # Интерполируем хорду/крутку из 4 контрольных точек агента во все
    # ~14 сечений модели. clip прижимает внутренние (внутри ступицы)
    # сечения к первой контрольной станции.
    x = np.clip(BLADE_STATIONS / R, STATIONS[0], 1.0)
    chords = np.interp(x, STATIONS, chord_st)
    betas = np.interp(x, STATIONS, twist_st)

    # Строим 3D-контур в каждом сечении.
    secs = [section_3d(r, c, b)
            for r, c, b in zip(BLADE_STATIONS, chords, betas)]

    tris = []
    m = len(AIRFOIL)
    # Боковая поверхность: каждую пару соседних сечений соединяем
    # "поясом" из треугольников (каждая ячейка пояса = 2 треугольника).
    # Нюанс ориентации: контур CCW в координатах (x, z), но при взгляде
    # вдоль -Y он выглядит CW, поэтому порядок вершин в треугольниках
    # обратный — иначе нормали стенок смотрели бы внутрь тела.
    for k in range(len(secs) - 1):
        a, b = secs[k], secs[k + 1]
        for i in range(m):
            j = (i + 1) % m
            tris.append((a[i], b[j], a[j]))
            tris.append((a[i], b[i], b[j]))

    # Торцевые крышки: триангулируем контур профиля и накрываем им
    # корневое и концевое сечения (у кончика порядок вершин обратный,
    # чтобы нормаль смотрела наружу в +Y, у корня — в -Y).
    cap = triangulate_2d(AIRFOIL)
    root, tip = secs[0], secs[-1]
    for (i, j, k) in cap:
        tris.append((root[i], root[j], root[k]))             # нормаль -Y
        tris.append((tip[i], tip[k], tip[j]))                # нормаль +Y
    return ensure_outward(np.array(tris))


def hub_mesh():
    """Строит оболочку ступицы — трубу с каналом под вал.

    Четыре поверхности: наружная цилиндрическая стенка, внутренний канал
    вала, верхнее кольцо и нижнее кольцо (торцы трубы).

    Выход: массив треугольников (T, 3, 3) с нормалями наружу.
    """
    # HUB_SEG точек равномерно по окружности (без дублирования 0 и 2*pi).
    a = np.linspace(0, 2 * np.pi, HUB_SEG, endpoint=False)
    ca, sa = np.cos(a), np.sin(a)

    def ring(radius, z):
        """Кольцо точек радиуса radius на высоте z: (HUB_SEG, 3)."""
        return np.stack([radius * ca, radius * sa,
                         np.full(HUB_SEG, z)], axis=1)

    # Четыре кольца: наружные низ/верх (ob/ot) и внутренние низ/верх (ib/it_).
    ob, ot = ring(HUB_R_OUT, -HUB_HALF_H), ring(HUB_R_OUT, HUB_HALF_H)
    ib, it_ = ring(HUB_R_BORE, -HUB_HALF_H), ring(HUB_R_BORE, HUB_HALF_H)
    tris = []
    for i in range(HUB_SEG):
        j = (i + 1) % HUB_SEG   # следующая точка по кругу (с замыканием)
        # Каждая четвёрка точек (ячейка поверхности) = 2 треугольника.
        # Порядок вершин задаёт направление нормали (правило правой руки).
        tris += [(ob[i], ob[j], ot[j]), (ob[i], ot[j], ot[i])]      # наружная стенка
        tris += [(ib[i], it_[j], ib[j]), (ib[i], it_[i], it_[j])]   # канал (нормали внутрь канала = наружу тела)
        tris += [(ot[i], ot[j], it_[j]), (ot[i], it_[j], it_[i])]   # верхнее кольцо
        tris += [(ob[i], ib[j], ob[j]), (ob[i], ib[i], ib[j])]      # нижнее кольцо
    return ensure_outward(np.array(tris))


def shell_volume(tris):
    """Объём замкнутой оболочки по формуле дивергенции.

    Сумма смешанных произведений вершин каждого треугольника / 6 даёт
    объём тела. Знак говорит об ориентации: > 0 = нормали наружу.
    """
    return float(np.einsum("ij,ij->", tris[:, 0],
                           np.cross(tris[:, 1], tris[:, 2]))) / 6.0


def ensure_outward(tris):
    """Гарантирует нормали наружу: если объём отрицательный —
    переворачивает порядок вершин во всех треугольниках."""
    if shell_volume(tris) < 0:
        tris = tris[:, ::-1, :]
    return tris


def check_shell(tris, name):
    """Проверяет, что оболочка пригодна для 3D-печати.

    Два условия:
      1. Водонепроницаемость: каждое ребро принадлежит РОВНО двум
         треугольникам. Ребро с 1 треугольником = дыра, с 3+ = самопересечение.
      2. Положительный объём: нормали смотрят наружу.

    Падает с assert (и понятным сообщением), если что-то не так.
    Возвращает объём оболочки, м3.
    """
    edges = {}
    for t in tris:
        for e in ((0, 1), (1, 2), (2, 0)):
            # Ключ ребра: пара округлённых вершин, отсортированная, чтобы
            # ребро (A,B) и (B,A) считались одним и тем же ребром.
            key = tuple(sorted((tuple(np.round(t[e[0]], 7)),
                                tuple(np.round(t[e[1]], 7)))))
            edges[key] = edges.get(key, 0) + 1
    bad = sum(1 for c in edges.values() if c != 2)
    assert bad == 0, f"{name}: не водонепроницаема, {bad} рёбер"
    vol = shell_volume(tris)
    assert vol > 0, f"{name}: нормали внутрь"
    return vol


def write_stl(path, tris_mm):
    """Пишет треугольники в бинарный STL-файл.

    Формат бинарного STL прост:
      * 80 байт заголовка (любой текст),
      * 4 байта — число треугольников (uint32),
      * на каждый треугольник: нормаль (3 float) + 3 вершины (по 3 float)
        + 2 байта атрибутов (не используются).
    struct.pack("<...") — упаковка чисел в байты, little-endian.

    Вход: path — путь к файлу; tris_mm (T, 3, 3) — треугольники в мм.
    """
    with open(path, "wb") as f:
        f.write(b"RL-optimized quiet 13in propeller".ljust(80, b"\0"))
        f.write(struct.pack("<I", len(tris_mm)))
        for t in tris_mm:
            # Нормаль = нормированное векторное произведение двух сторон.
            n = np.cross(t[1] - t[0], t[2] - t[0])
            ln = np.linalg.norm(n)
            f.write(struct.pack("<3f", *(n / ln if ln > 0 else n)))
            for p in t:
                f.write(struct.pack("<3f", *p))
            f.write(struct.pack("<H", 0))


if __name__ == "__main__":
    import sys
    here = pathlib.Path(__file__).parent

    # Какой JSON читать — из аргумента командной строки (по умолчанию тихий винт).
    # запуск: python3 prop_stl.py [best_prop.json | best_prop_thrust.json]
    src = sys.argv[1] if len(sys.argv) > 1 else "best_prop.json"
    best = json.loads((here / src).read_text())

    # В JSON параметры хранятся в "человеческих" единицах (мм, градусы) —
    # переводим обратно в метры и радианы для геометрии.
    chord_st = np.array(best["chord_mm"]) / 1000.0
    twist_st = np.deg2rad(best["twist_deg"])

    # Строим тела. Вторая лопасть = первая, отражённая по X и Y
    # (это эквивалент поворота на 180° вокруг оси винта).
    blade1 = blade_mesh(chord_st, twist_st)
    blade2 = blade1 * np.array([-1.0, -1.0, 1.0])
    blade2 = ensure_outward(blade2)   # отражение переворачивает нормали — чиним
    hub = hub_mesh()

    # Проверяем каждую оболочку (упадёт с assert, если модель дырявая).
    v1 = check_shell(blade1, "лопасть 1")
    v2 = check_shell(blade2, "лопасть 2")
    vh = check_shell(hub, "ступица")

    # Склеиваем всё в один список, переводим метры -> мм,
    # опускаем модель так, чтобы низ лежал на столе принтера (z=0).
    tris = np.concatenate([blade1, blade2, hub]) * MM
    tris[:, :, 2] -= tris[:, :, 2].min()

    suffix = "_thrust" if best.get("objective") == "thrust" else ""
    out = here / f"prop_13in{suffix}.stl"
    write_stl(out, tris)

    # Итоговая сводка: габариты, масса при 100% заполнении PLA, режим работы.
    lo = tris.reshape(-1, 3).min(axis=0)
    hi = tris.reshape(-1, 3).max(axis=0)
    mass_pla = (v1 + v2 + vh) * 1e9 * 1.24e-3    # м3 -> мм3, плотность PLA 1.24 г/см3
    print(f"STL: {out}")
    print(f"Оболочки водонепроницаемы. Треугольников: {len(tris)}")
    print(f"Габариты: {hi[0]-lo[0]:.0f} x {hi[1]-lo[1]:.0f} x {hi[2]-lo[2]:.1f} мм")
    print(f"Масса (100% PLA): {mass_pla:.0f} г")
    print(f"Режим: {best['rpm']:.0f} об/мин -> тяга "
          f"{best['thrust_n']/9.81*1000:.0f} г, {best['power_w']:.0f} Вт, "
          f"~{best['spl_db']:.0f} дБ, V_tip {best['v_tip']:.0f} м/с")
