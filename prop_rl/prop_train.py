"""Обучение агента алгоритмом REINFORCE: поиск самого тихого 13" винта с тягой >= 12 Н.

КАК ЭТО РАБОТАЕТ (кратко для тех, кто не знаком с RL)
-----------------------------------------------------
Задача одношаговая ("бандит"): агент выдаёт 9 чисел (4 хорды + 4 крутки
+ обороты), среда сразу возвращает награду. Никаких "состояний" и
"переходов" — просто оптимизация функции, которую нельзя продифференцировать
напрямую (внутри итерационная физика BEMT).

Политика агента — гауссова (нормальное распределение): у каждого из
9 действий есть среднее mu и разброс std. Обучаемые параметры — сами
mu и log_std (логарифм разброса — чтобы std всегда был положительным).

Алгоритм REINFORCE на каждой итерации:
  1. Сэмплируем батч случайных действий из текущего распределения.
  2. Считаем награду каждого действия через среду (prop_env.evaluate).
  3. Сдвигаем mu/std так, чтобы действия с наградой ВЫШЕ средней стали
     вероятнее, а с наградой ниже средней — менее вероятны.
Повторяем — распределение постепенно "стягивается" к хорошим дизайнам.

Награда = -шум при выполнении ограничений по тяге, мощности и запасу до
срыва. Лучший ДОПУСТИМЫЙ дизайн (все ограничения выполнены) сохраняется
в best_prop.json — его потом печатает в STL скрипт prop_stl.py.

Запуск:
    python3 prop_train.py            # цель "quiet"  -> best_prop.json
    python3 prop_train.py thrust     # цель "thrust" -> best_prop_thrust.json
"""

import json
import pathlib
import sys

import numpy as np
import torch

from prop_env import evaluate, TARGET_THRUST_N, POWER_LIMIT_W

# Цель оптимизации берём из аргумента командной строки; по умолчанию "quiet".
OBJECTIVE = sys.argv[1] if len(sys.argv) > 1 else "quiet"
assert OBJECTIVE in ("quiet", "thrust")

# ---- Гиперпараметры обучения --------------------------------------------------
ITERS = 600     # сколько итераций обучения (шагов градиентного спуска)
BATCH = 96      # сколько случайных дизайнов пробуем на каждой итерации
LR = 0.03       # learning rate — размер шага оптимизатора
SEED = 0        # фиксируем случайность, чтобы результат воспроизводился

torch.manual_seed(SEED)
np.random.seed(SEED)

# ---- Параметры политики --------------------------------------------------------
# mu — средние значения 9 действий (старт: нули = середины диапазонов
# после tanh в среде); log_std — логарифмы разбросов (старт: log(1)=0,
# т.е. std=1 — широкий поиск в начале).
# requires_grad=True говорит PyTorch считать градиенты по этим тензорам.
mu = torch.zeros(9, requires_grad=True)
log_std = torch.zeros(9, requires_grad=True)
opt = torch.optim.Adam([mu, log_std], lr=LR)   # Adam — стандартный оптимизатор

# Критерий "этот дизайн лучше текущего лучшего?" зависит от цели:
# для thrust — максимальная тяга, для quiet — минимальный шум.
# cur.get с дефолтом обрабатывает случай "лучшего ещё нет" (пустой словарь).
if OBJECTIVE == "thrust":
    better = lambda info, cur: info["thrust_n"] > cur.get("thrust_n", -1.0)
else:
    better = lambda info, cur: info["spl_db"] < cur.get("spl_db", float("inf"))
best = {}   # сюда копим метрики лучшего допустимого дизайна за всё обучение

# ---- Главный цикл обучения ------------------------------------------------------
for it in range(ITERS):
    # std = exp(log_std), с ограничением: не даём разбросу схлопнуться
    # ниже 0.03 (иначе поиск останавливается слишком рано) и раздуться
    # выше 2.0 (иначе поиск — чистый шум).
    std = log_std.exp().clamp(0.03, 2.0)

    # Строим нормальное распределение и сэмплируем батч действий (96 x 9).
    dist = torch.distributions.Normal(mu, std)
    actions = dist.sample((BATCH,))

    # log_prob — логарифм вероятности каждого действия. Суммируем по
    # 9 компонентам (вероятность вектора = произведение вероятностей
    # компонент = сумма логарифмов). Это ключевая величина REINFORCE.
    logp = dist.log_prob(actions).sum(dim=1)

    # Прогоняем все дизайны через физику. .numpy() — потому что среда
    # написана на numpy; градиенты через физику нам и не нужны.
    rewards, feasible, infos = evaluate(actions.numpy(), OBJECTIVE)

    # Обновляем "лучший найденный допустимый дизайн".
    for ok, info in zip(feasible, infos):
        if ok and better(info, best):
            best = info

    # ---- Шаг REINFORCE ----
    r = torch.as_tensor(rewards, dtype=torch.float32)

    # Advantage ("преимущество"): награда минус средняя по батчу.
    # Вычитание среднего (baseline) — стандартный приём для снижения
    # шума градиента: важно не абсолютное значение награды, а лучше ли
    # этот дизайн, чем остальные в батче.
    adv = r - r.mean()

    # Функция потерь REINFORCE: -(logp * adv).
    # Минимизация этого = повышение вероятности действий с adv > 0
    # и понижение с adv < 0. Второй член (-entropy) — бонус за энтропию:
    # слегка мешает распределению схлопнуться, поддерживая исследование.
    loss = -(logp * adv).mean() - 1e-3 * dist.entropy().sum()

    # Классическая тройка PyTorch: обнулить градиенты, посчитать новые,
    # сделать шаг оптимизатора.
    opt.zero_grad()
    loss.backward()
    opt.step()

    # Печатаем прогресс каждые 60 итераций (и на последней).
    if it % 60 == 0 or it == ITERS - 1:
        tag = (f"{best.get('thrust_n', float('nan')):.1f} Н"
               if OBJECTIVE == "thrust"
               else f"{best.get('spl_db', float('nan')):.1f} дБ")
        print(f"iter {it:3d} | reward {rewards.mean():7.2f} | "
              f"feasible {feasible.mean()*100:5.1f}% | best {tag}")

# Если за всё обучение ни один дизайн не выполнил ограничения — это ошибка
# (обычно значит, что ограничения слишком жёсткие или обучение сломано).
assert "rpm" in best, "не найдено ни одного допустимого винта"

# ---- Сохранение результата -------------------------------------------------------
# Дописываем в словарь контекст задачи, чтобы JSON был самодостаточным.
best["objective"] = OBJECTIVE
best["target_thrust_n"] = TARGET_THRUST_N
best["power_limit_w"] = POWER_LIMIT_W

# Имя файла зависит от цели — чтобы два запуска не перезаписывали друг друга.
name = "best_prop.json" if OBJECTIVE == "quiet" else "best_prop_thrust.json"
out = pathlib.Path(__file__).parent / name
out.write_text(json.dumps(best, indent=2))

# Итоговая сводка в консоль.
print(f"\nЛучший винт: тяга {best['thrust_n']:.1f} Н "
      f"({best['thrust_n']/9.81*1000:.0f} г) "
      f"@ {best['rpm']:.0f} об/мин, {best['power_w']:.0f} Вт, "
      f"{best['spl_db']:.1f} дБ, V_tip {best['v_tip']:.0f} м/с")
print(f"Хорды {best['chord_mm']} мм, крутка {best['twist_deg']} град")
print(f"Эффективность {best['efficiency_g_w']:.1f} г/Вт")
print(f"Сохранено: {out}")
